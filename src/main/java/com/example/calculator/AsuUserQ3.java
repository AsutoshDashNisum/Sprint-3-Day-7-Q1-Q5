package com.example.calculator;

public class AsuUserQ3 {
    private String name;
    private String email;
    private int age;

    public AsuUserQ3(String name, String email, int age) {
        this.name = name;
        this.email = email;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public int getAge() {
        return age;
    }
}
